def checkio(maze_map):
    
    rows = 12
    columns = 12
    routs = [[0 for x in range(columns)] for x in range(rows)]
    routs[1][1] = ''
    neighbours = [('E',0,1), ('S', 1, 0), ('W', 0, -1), ('N', -1, 0)]
    while True:
        for i in range(1,rows-1):
            for j in range(1, columns-1):
                if maze_map[i][j] == 0 and routs[i][j] != 0:
                    for (direct, shift_row,shift_col) in neighbours:
                        new_row = i+shift_row
                        new_col = j+shift_col
                        if routs[new_row][new_col] == 0 and maze_map[new_row][new_col] == 0:
                            routs[new_row][new_col] = routs[i][j] + direct
                            if new_row == 10 and new_col == 10:
                                return routs[new_row][new_col]
            
    #This is just example for first maze
    return "SSSSSEENNNEEEEEEESSWWWWSSSEEEESS"


